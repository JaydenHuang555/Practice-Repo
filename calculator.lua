local x = io.read()

print("Input Operation: ")
local op = io.read()

print("Second Number: ")
local y = io.read()

if op == "+" then
	print(x+y)
end

if op == "-" then
        print(x-y)
end

if op == "*" then
        print(x*y)
end

if op == "/" then
        print(x/y)
end

if op == "%" then
        print(x%y)
end

if op == "^" then
        print(x^y)
end


